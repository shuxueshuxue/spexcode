#!/usr/bin/env node
// Generates spec-cli/src/migrate-table.ts — the known-old-template hash table `spex doctor --migrate`
// matches adopter plugin assets against. NEVER hand-edit the output: the table is derived from this
// repo's own git history (every blob version each `.config/<rel>` template asset ever had, across the
// templates tree, the preset packages, and spexcode's own live `.spec` copy — adopters were seeded from
// the first two and ops-patched from the third). Re-run after any release that changes templates history:
//   node scripts/gen-migrate-table.mjs
import { execFileSync } from 'node:child_process'
import { writeFileSync, readdirSync, statSync } from 'node:fs'
import { join, relative } from 'node:path'
import { fileURLToPath } from 'node:url'

const REPO = fileURLToPath(new URL('..', import.meta.url))
const git = (args) => execFileSync('git', ['-C', REPO, ...args], { encoding: 'utf8', maxBuffer: 64 * 1024 * 1024 })

// every historical (path, blobSha) pair under the old-world `.config` locations, on main's history.
// --no-renames keeps each rename as a plain A/D so every version surfaces with its full blob sha.
const PREFIXES = ['spec-cli/templates/spec/project/.config', '.spec/spexcode/.config', 'spec-cli/templates/presets']
const raw = git(['log', '--format=%H', '--raw', '--no-abbrev', '--no-renames', 'main', '--', ...PREFIXES])

// a historical path → its plugin-root-relative path, e.g.
//   spec-cli/templates/spec/project/.config/core/idle/idle.sh → core/idle/idle.sh
//   spec-cli/templates/presets/careful/.config/clarify-before-code/spec.md → clarify-before-code/spec.md
//   .spec/spexcode/.config/core/stop-gate/stop-gate.sh → core/stop-gate/stop-gate.sh
function relUnderConfig(path) {
  const m = path.match(/(?:^|\/)\.config\/(.+)$/)
  return m ? m[1] : null
}

const hist = new Map() // rel → Set<blobSha>
for (const line of raw.split('\n')) {
  // raw line: ":100644 100644 <old> <new> <status>\t<path>"
  const m = line.match(/^:\d+ \d+ [0-9a-f]{40} ([0-9a-f]{40}) [A-Z]\d*\t(.+)$/)
  if (!m) continue
  const [, sha, path] = m
  if (sha === '0'.repeat(40)) continue // deletion — the content versions were already recorded when added
  const rel = relUnderConfig(path)
  if (!rel) continue
  if (!hist.has(rel)) hist.set(rel, new Set())
  hist.get(rel).add(sha)
}

// the replaceable set = assets the SHIPPED package still carries (templates/**/.plugins/<rel>) — those are
// the only ones the migrator has new content for at runtime. Project templates win over preset packages
// (they never overlap today; assert that).
function walk(dir) {
  let out = []
  for (const e of readdirSync(dir)) {
    const p = join(dir, e)
    if (statSync(p).isDirectory()) out = out.concat(walk(p))
    else out.push(p)
  }
  return out
}
const newRoots = [join(REPO, 'spec-cli/templates/spec/project/.plugins')]
for (const e of readdirSync(join(REPO, 'spec-cli/templates/presets'))) newRoots.push(join(REPO, 'spec-cli/templates/presets', e, '.plugins'))
const templateSrc = new Map() // rel → path relative to spec-cli/ (runtime PKG_ROOT), for the new content
for (const root of newRoots) {
  for (const f of walk(root)) {
    const rel = relative(root, f)
    if (templateSrc.has(rel)) throw new Error(`template rel path collides across roots: ${rel}`)
    templateSrc.set(rel, relative(join(REPO, 'spec-cli'), f))
  }
}

const entries = []
for (const rel of [...templateSrc.keys()].sort()) {
  const shas = [...(hist.get(rel) ?? [])].sort()
  entries.push({ rel, template: templateSrc.get(rel), oldShas: shas })
}
// historical assets with NO shipped counterpart (retired templates) — the migrator can recognize but not
// replace them; list them so it can say "stock but retired" instead of "customized".
const retired = [...hist.keys()].filter((rel) => !templateSrc.has(rel)).sort()

// every blob the spexcode `config` node's spec.md ever had — the hash gate for the config→plugin-system
// node rename: an adopter's node named `config` is only renamed when its spec.md IS a stock copy of
// spexcode's plugin-system spec (the rehearsal tree is one); an adopter's own domain node named `config`
// matches nothing and is flagged, never renamed.
const cfgRaw = git(['log', '--format=%H', '--raw', '--no-abbrev', '--no-renames', 'main', '--', '.spec/spexcode/config/spec.md'])
const configShas = new Set()
for (const line of cfgRaw.split('\n')) {
  const m = line.match(/^:\d+ \d+ [0-9a-f]{40} ([0-9a-f]{40}) [A-Z]\d*\t\.spec\/spexcode\/config\/spec\.md$/)
  if (m && m[1] !== '0'.repeat(40)) configShas.add(m[1])
}

const body = `// GENERATED by scripts/gen-migrate-table.mjs — do not hand-edit. See [[migrate]].
// For each plugin asset the shipped templates still carry: every git blob sha its OLD-WORLD (.config-era)
// content ever had on main. \`spex doctor --migrate\` hashes an adopter's file; a hit on oldShas means the
// file is a stock historical template → safe to replace wholesale with the shipped template. Anything
// else is flagged for review, never rewritten.
export type MigrateAsset = { rel: string; template: string; oldShas: string[] }
export const TEMPLATE_ASSETS: MigrateAsset[] = ${JSON.stringify(entries, null, 2)}
// old-world template assets the shipped package no longer carries (recognized → reported, not replaced):
export const RETIRED_ASSETS: Record<string, string[]> = ${JSON.stringify(Object.fromEntries(retired.map((r) => [r, [...hist.get(r)].sort()])), null, 2)}
// every historical blob of spexcode's own \`config\` node spec.md (the plugin-system spec, pre-rename) —
// gates the config→plugin-system node rename on adopter trees:
export const CONFIG_NODE_SHAS: string[] = ${JSON.stringify([...configShas].sort(), null, 2)}
`
writeFileSync(join(REPO, 'spec-cli/src/migrate-table.ts'), body)
console.log(`wrote spec-cli/src/migrate-table.ts — ${entries.length} shipped asset(s), ${retired.length} retired, ${[...hist.values()].reduce((n, s) => n + s.size, 0)} historical blob version(s), ${configShas.size} config-node blob(s)`)
